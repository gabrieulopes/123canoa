    /**
     * M√©todo para validar os campos e inserir os dados do usu√°rio a se cadastrodo.
     * Caso for validado com sucesso ser√° cadastrado, sen√£o n√£o ser√° cadastrado.
     */
    private void validaCadastro(){
        String nome = txtNome.getText();
        String login = txtLogin.getText();
        String senha = new SecurityDada().criptString(txtSenha.getText());
        String perfil = txtPerfil.getText();
            if(new ValideCampoText().validaCampoLogin(txtLogin.getText())){
                lblMessagemLogin.setForeground(Color.red);
                lblMessagemLogin.setText("Login precisa no m√≠nino 8 digitos");
            }else if(new ValideCampoText().validaCampoSenha(txtSenha.getText())){
                lblMessagemSenha.setForeground(Color.red);
                lblMessagemSenha.setText("Senha precisa no m√≠nino 8 digitos");
            }else if(new UsuariosController().verificaNullCampos(nome, login, senha, perfil)){
                JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Aten√ß√£o", JOptionPane.WARNING_MESSAGE);
            }else{
                // FAZER O CADASTRO DO USU√ÅRIO   
                if(new UsuariosController().validar(nome, login, senha, perfil)){
                    excluirCampos();
                    JOptionPane.showMessageDialog(this, "Cadastrado com sucesso!", "Cadastrado", JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(this, "Erro ao cadastrar! Talvez o login ja exista", "N√£o cadastrado", JOptionPane.ERROR_MESSAGE);
                }
            } 
    }
    
    /**
     * M√©todo para excluir os campos, recomenda-se quando for cadastrado com sucesso um usu√°rio.
     * <br><br>Obs: Os campos a serem exclu√≠dos s√£o (nome, login e senha).
     */
    private void excluirCampos(){
        txtNome.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
    }
    
