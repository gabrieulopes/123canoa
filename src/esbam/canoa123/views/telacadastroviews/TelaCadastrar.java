    // ESCOLHA DOS PAINEIS PELO ITEM SELECIONADO
    private void registroEscolhido(String nome) {
        CardLayout cl = (CardLayout) this.paneMain.getLayout();
        try {
            if (nome.equalsIgnoreCase("barco")) {
                cl.show(this.paneMain, "paneOptionCadastroBarco");
            } else if (nome.equalsIgnoreCase("rota")) {
                cl.show(this.paneMain, "paneOptionCadastroRota");
            } else if (nome.equalsIgnoreCase("cidade")){
                cl.show(this.paneMain, "paneOptionCadastroCidade");
            } else if (nome.equalsIgnoreCase("categoria")){
                cl.show(this.paneMain, "paneOptionCadastroCategoria");
            }
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }

