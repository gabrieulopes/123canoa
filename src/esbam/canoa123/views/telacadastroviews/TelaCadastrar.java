    // INICIALIZADORES
    private void addSelectItem(){    
        new CidadesController().listaCidades().forEach((v) -> { // TRAZENDO A LISTA CONSULTADA DAS CIDADES REGISTRADAS
            jcbDestino.addItem(v.getLocalidade());
            jcbOrigem.addItem(v.getLocalidade());
        });
        if(jcbDestino.getItemCount() > 2){ // INICIANDO O ITEM COM OS DOIS PRIMEIROS (IGNORANDO O "SELECIONE")
            txtOrigemRota.setText(jcbOrigem.getItemAt(1));
            txtDestinoRota.setText(jcbDestino.getItemAt(2));            
            jcbDestino.setSelectedIndex(2);
            jcbOrigem.setSelectedIndex(1);
        }
        new CategoriasController().lista().forEach((v) -> { jcbCategoria.addItem(v.getNome()); });
        if(jcbCategoria.getItemCount() > 1){
            txtCategoriaRota.setText(jcbCategoria.getItemAt(1));
            jcbCategoria.setSelectedIndex(1);
        }
    }
    
