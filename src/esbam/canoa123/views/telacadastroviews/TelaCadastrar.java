    private void jcbDestinoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbDestinoItemStateChanged
        if(!jcbDestino.getSelectedItem().toString().equalsIgnoreCase("selecione") ){
            if(jcbDestino.getSelectedItem().toString().equalsIgnoreCase(jcbOrigem.getSelectedItem().toString())){      
                txtDestinoRota.setText("");
                JOptionPane.showMessageDialog(null, "Essa cidade está selecionada", "Atenção", JOptionPane.WARNING_MESSAGE);
            }else{
                txtDestinoRota.setText(jcbDestino.getSelectedItem().toString());
            }
        }else{
            txtDestinoRota.setText("");
        }
    }//GEN-LAST:event_jcbDestinoItemStateChanged

