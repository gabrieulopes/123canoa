    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            if(paneOptionCadastroBarco.isVisible()){
                // BARCO
                RegistraDados regBarco = new RegistraDados();
                regBarco.registraBarco(txtNomeBarco, txtCapacidadeBarco);
            }else if(paneOptionCadastroRota.isVisible()){
                // ROTA
                RegistraDados regRota = new RegistraDados();
                regRota.registraRota(txtValorRota, txtOrigemRota, txtDestinoRota, txtCategoriaRota);
            }else if(paneOptionCadastroCidade.isVisible()){
                // CIDADE
                RegistraDados regCidade = new RegistraDados();
                regCidade.registraCidade(txtEstadoCidade, txtCidadeCidade);
            }else if(paneOptionCadastroCategoria.isVisible()){
                // CATEGORIA
                RegistraDados regCategoria = new RegistraDados();
                regCategoria.registrarCategoria(txtNomeCategoria);
            }  
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Não foi possível executar os métodos de registros", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

