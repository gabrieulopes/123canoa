    // EVENTO QUE VAI VERIFICAR O ESTADO DOS ITENS (A TROCA) E REALIZAR ALGUMA COISA (A SELEÇÃO DA IMAGEM)
    private void cmbItensRegistroItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbItensRegistroItemStateChanged
        String regEscolha = cmbItensRegistro.getSelectedItem().toString();
        lblImage.setIcon(
        (regEscolha.equalsIgnoreCase("Barcos")) ? 
        alterarImg(1) : 
        (regEscolha.equalsIgnoreCase("Rota")) ?
        alterarImg(2) : 
        (regEscolha.equalsIgnoreCase("Cidade")) ?
        alterarImg(3) : 
        (regEscolha.equalsIgnoreCase("Categoria")) ?
        alterarImg(4) : null
        );
        lblImage.updateUI();
    }//GEN-LAST:event_cmbItensRegistroItemStateChanged

    /**
     * 
     * @param indexIem
     * @return 
     */
    // METODO DE ESCOLHA DA IMAGEM
    private ImageIcon alterarImg(int indexIem){
        ImageIcon newImg = new ImageIcon();        
        switch (indexIem) {
            case 1: 
                newImg.setImage(altDimen("barco"));
                return newImg;  
            case 2:
                newImg.setImage(altDimen("rota"));
                return newImg;   
            case 3:
                newImg.setImage(altDimen("cidade"));
                return newImg;
            case 4:
                newImg.setImage(altDimen("categoria"));
                return newImg;
            default:
                return null;
        }
    }
    
