    // MODIFICADORES
    private java.awt.Image altDimen(String nomeImg){
        BufferedImage img = null;
        try{
            img = javax.imageio.ImageIO.read(new java.io.File(String.format
            ("C:\\Projetos\\Gerecionamento\\Canoa123\\src\\esbam\\canoa123\\img\\%s.png", nomeImg)));
        }catch(java.io.IOException ex){
            lblImage.setAlignmentX(CENTER_ALIGNMENT);
            lblImage.setAlignmentY(CENTER_ALIGNMENT);
            lblImage.setText("<html>Erro ao carregar imagem</html>");
            return null;
        }
        // CONFIGURAR A IMAGE E ATRIBUI-LA AO OBJETO DA CLASSE IMAGE
        // O TERCEIRO PARAMETRO DO METODO "GETSCALEDINSTANCE" E DIZER QUE O NOVO DIMENSIONAMENTO SEJA SUAVE
        java.awt.Image imageDimensionada = img.getScaledInstance(lblImage.getWidth() - 12, lblImage.getHeight() - 12, java.awt.Image.SCALE_SMOOTH);      
        return imageDimensionada;
    }

