    /**
     * 
     */
    private void pesquisa(){
        try {
            String nome = txtPesquisaNome.getText();
            excluirRows();
            addRowsTable(new PassageirosController().listNomePassageiros(nome), new PassagensController().listaPassagens());
        } catch (Exception e) {
            excluirRows();
            executeLista();
        }
    }
    
    /**
     * 
     * @param passageiro
     * @param passagem 
     */
    private void addRowsTable(ArrayList<Passageiros> passageiro, ArrayList<Passagens> passagem){
        passageiro.forEach((v) -> {
                int i = table.getRowCount();
                boolean comparatorID = false;
                String semPassagem = "NULL";
                while(comparatorID != true){
                    if(rowIDPassagem == passagem.size() || comparatorID){
                        break;
                    }
                    rowIDPassagem++;
                    comparatorID = passageiro.get(i).getId() == passagem.get(rowIDPassagem - 1).getId_passageiro().getId();
                }
                table.addRow(new Object[] {
                    passageiro.get(i).getId(),
                    passageiro.get(i).getNome(),
                    passageiro.get(i).getCpf(),
                    passageiro.get(i).getSexo(),
                    passageiro.get(i).getTelefone(),
                    (comparatorID) ? passagem.get(rowIDPassagem - 1).getId() : semPassagem
                });
            rowIDPassagem = 0;
        });
    }
